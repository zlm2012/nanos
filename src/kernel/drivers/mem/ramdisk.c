static void ramdisk_driver_thread(void);

void
init_ramdisk(void) {
  PCB *p = create_kthread(ramdisk_driver_thread);
  RAMDISK = p->pid;
  hal_register("ram", RAMDISK, 0);
  wakeup(p);
}

static void
ramdisk_driver_thread(void) {
  static Msg m;

  while (true) {
    receive(ANY, &m);

    if (m.type == DEV_READ) {
      copy_from_kernel(fetch_pcb(m.req_pid), m.buf, disk+m.offset, m.len);
      m.ret = m.len;
      m.dest = m.src;
      m.src = RAMDISK;
      send(m.dest, &m);
    } else if (m.type == DEV_WRITE) {
      uint32_t i;
      uint8_t data;
      for (i = 0; i < m.len; i ++) {
	copy_to_kernel(fetch_pcb(m.req_pid), &data, m.buf + i, 1);
	memcpy(&(disk[m.offset + i]), &data, 1);
      }
      m.ret = i;
      m.dest = m.src;
      m.src = RAMDISK;
      send(m.dest, &m);
    }
    else {
      assert(0);
    }
  }
}
